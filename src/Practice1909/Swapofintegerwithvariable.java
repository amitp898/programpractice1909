package Practice1909;

public class Swapofintegerwithvariable {

    public static void main(String[]args){
        int a = 5;
        int b = 6;
        int c;
        System.out.println("Before Swap:"+ a  +" "+ b);
        c = a;
        a = b;
        b = c;
        System.out.println("After Swap:"+ a +" "+ b);
    }
}
